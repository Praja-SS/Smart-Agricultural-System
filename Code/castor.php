          <h3 class="center">CASTOR</h3><br>
		  <img class="responsive-img" src="castor.jpg" style="width:300px;height:200px;">
		 </div>
         <!-- insert content -->
        <p>
          <br>
		  <b>Introduction:</b>
			Castor is one of the oldest cultivated crops; however, it contributes to only 0.15% of the vegetable oil produced in the world. The oil produced from this crop is considered to be of importance to the global specialty chemical industry because it is the only commercial source of a hydroxylated fatty acid.
			<br><br>
			<b>Land preparation:</b>
      Plough two-three times with country or mould board plough.Spread  compost evenly on the main field before last ploughing and incorporate in to soil by working a country plough. Apply gypsum at the time of last ploughing for higher castor yield.

		   <br><br>
		   <b>Climate:</b>
		   It grows in tropical and subtropical regions as a perennial plant and in temperate climate as an annual plant. A moderate temperature of 20-260C is highly favourable during crop period for obtaining higher yields.
			<br><br>
			<b>Soil:</b>
			The most suitable soils for castor cultivation are deep, moderately fertile, with slightly acidic conditions and well drained sandy loam soils. The ideal soil pH of soils should be around 6.0 for better yield.
			<br><br>
			<b>Sowing:</b>
			<br><b>Time of sowing:</b> Sowings commence in the month of May and extended upto middle of July<br><b>
Spacing:</b> Wide rows with the row spacing of 60 cm and distance of 60 cm between plants .<br>
<b>Method of sowing:</b>The seed should be sown either by plough furrow or with a seed-drill or by hand-dibbling.<br>
<b>Sowing depth:</b> 4â€“7.5 cm deep  
			 <br><br>
			<b>Irrigation:</b>
      Rainfed crops dont need any irrigation.However in irrigated crop, water should be provided weekly twice.Frequency of irrigation depends on soil type,climate and crop stage.
			<br><br>
			<b>Harvest:</b>
			The maturity of the crop depends on the variety.Most of the improved cultivers mature in about 140-175 days.<br>
		 The easy indication of castor maturity is when 1 or 2 capsules in bunch show signs of drying. <br>
		 The whole cluster should be removed and stacked.<br>
     The harvesting of unripe capsules has an adverse affect on the oil content of the seed and these should be avoided.<br>
     It is recimmended to collect the fruits when they ripen.<br>
     After collecting the entire crop, it should be dried in sun for a couple of days and threshing should be done by beating the dried capsules with the help of stick.<br>
     Winnowing should be done in the usual manner.<br>
     One can use Castor Sheller as well to seperate the seeds.<br>
