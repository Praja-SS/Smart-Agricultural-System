                <div class="collapsible-header green-text text-darken-1">1. what is the farming and what are the different farming systems in india?</div>
                <div class="collapsible-body">The process of growing crops and rearing animals for food and raw materials like vegetables, fruits, milk, fibre, meat etc. is called farming. Under the broad spectrum of agriculture, farming is considered a small category which involves production of flowers, fruits, bio-fuels, drugs, fibres, <a target="https://www.agrifarming.in/plant-nursery-information-beginners" href="https://www.agrifarming.in/plant-nursery-information-beginners">nursery</a> plants, <a target="https://www.agrifarming.in/organic-manure-production-guide" href="https://www.agrifarming.in/organic-manure-production-guide">manure</a> and leather. Different types of farming systems practiced in India are:
                  <p class="flow-text"></p>
                  • Subsistence farming<p class="flow-text"></p>
                  • Shifting agriculture<p class="flow-text"></p>
                  • Plantation agriculture<p class="flow-text"></p>
                  • Intensive farming<p class="flow-text"></p>
                  • Dry agriculture<p class="flow-text"></p>
                  • Mixed farming<p class="flow-text"></p>
                  • Crop rotation<p class="flow-text"></p>
                  • Sedentary farming<p class="flow-text"></p>
                  • Terrace cultivation (hilly area farming)</div>
              </li>
          
              <li>
                 <div class="collapsible-header green-text text-darken-1">2. What is ley farming?</div>
                <div class="collapsible-body">Farming is very difficult in dry lands because of water scarcity
                and lack of soil fertility, so a method of farming was introduced to restore the fertility of the soil and was known as ley farming. In this method of farming, grasses were grown in rotation with food grains to boost the nutrient level of the soil. It acts as an insurance against crop failures due to drought conditions.</div>
              </li>
              <li>
                <div class="collapsible-header green-text text-darken-1">3. What is contour farming?</div>
                <div class="collapsible-body">The process of planting across a slope following its elevation contour lines. In this practice the ruts made by the plough are perpendicular to the slope rather than being parallel. Plants break the flow of water and prevent <a target="https://www.agrifarming.in/soil-erosion-causes-types-soil-conservation-methods" href="https://www.agrifarming.in/soil-erosion-causes-types-soil-conservation-methods">soil erosion</a>, thereby making contour farming a sustainable way of farming.</div>
              </li>
              <li>
                  <div class="collapsible-header green-text text-darken-1">4. What is hedgerow intercropping?</div>
                  <div class="collapsible-body">The practice of growing annual crops in between the rows of trees as an alternate method of fallow system is termed as hedgerow intercropping. The combination of trees and crops complement each other rather than competing. The trees create a favourable micro-climate for the crops to survive by shielding them from drying winds.</div>
              </li>
              <li>
                  <div class="collapsible-header green-text text-darken-1">5. What is a crop rotation?</div>
                  <div class="collapsible-body">The method of growing different types of crops in the same area during sequenced seasons is called <a target="https://www.agrifarming.in/crop-rotation-advantages-benefits-in-agriculture" href="https://www.agrifarming.in/crop-rotation-advantages-benefits-in-agriculture">crop rotation</a>. This is done mainly to prevent soil erosion due to mono-cropping. It is very clearly known that growing only a certain variety of crops in the same area depletes the region of one particular kind of nutrient, therefore, to keep or maintain the balance of nutrients in the soil a different crop is grown for a certain duration or season to help restore the imbalance.</div>
              </li>
              <li>
                <div class="collapsible-header green-text text-darken-1">6. How is soil useful in agriculture?</div>
                <div class="collapsible-body">The main idea of land preparation is to get the soil or field ready for planting and this may involve activities like removal of weeds and unwanted materials like stones, recycling plant nutrients, making the soil fine in texture etc. Land preparation has to be carried out either by zero-tillage or minimum tillage so as to keep the soil disturbance levels minimum because a totally puddled soil would have a destroyed structure for cultivation. Tilling the soil can be done by ploughing, harrowing and levelling.</div>
              </li>
              <li>
                <div class="collapsible-header green-text text-darken-1">7. What is the purpose of land preparation?</div>
                <div class="collapsible-body">The main idea of <a target="https://www.agrifarming.in/land-preparation-types-methods-objectives-advantages" href="https://www.agrifarming.in/land-preparation-types-methods-objectives-advantages">land preparation</a> is to get the soil or field ready for planting and this may involve activities like removal of weeds and unwanted materials like stones, recycling plant nutrients, making the soil fine in texture etc. Land preparation has to be carried out either by zero-tillage or minimum tillage so as to keep the soil disturbance levels minimum because a totally puddled soil would have a destroyed structure for cultivation. Tilling the soil can be done by ploughing, harrowing and levelling.</div>
              </li>
              <li>
                <div class="collapsible-header green-text text-darken-1">8. What is planting in agriculture?</div>
                <div class="collapsible-body">Incorporating the saplings or seeds in the main farming area after proper land preparation is called planting. It is basically of two types: direct seeding and transplanting.</div>
              </li>
              <li>
                <div class="collapsible-header green-text text-darken-1">9. Why tillage is important?</div>
                <div class="collapsible-body">Tilling is deep ploughing the land almost to the depth of 8-10 inches while preparing the soil for cultivation. When the soil condition is very poor, then a large quantity of <a target="https://www.agrifarming.in/organic-farming-faq-information" href="https://www.agrifarming.in/organic-farming-faq-information">organic</a> matter has to be incorporated into it and this can be done only by tilling the soil. Other important reasons for tilling the soil could be facilitating air circulation, decomposition, etc. Tilling can be done in many ways and is expected to have its own advantages and disadvantages.</div>
              </li>
              <li>
                <div class="collapsible-header green-text text-darken-1">10. What is land cleaning or clearing?</div>
                <div class="collapsible-body">The process of removing trees, stumps, brush, stones and other unwanted substances from the farm area is called land clearing. Doing so improves the productivity of the land and provides a new land base for next crop cycle. The land clearing process also involves piling up the debris and disposing it by burning the pile. Land is cleared using clearing equipment maintained by trained operators. Piling rakes and blades are also used while cleaning.</div>
              </li>
             <li>
                <div class="collapsible-header green-text text-darken-1">11. What are the factors that affect crop production?</div>
                  <div class="collapsible-body">The crop production cycle depends on two major factors, i.e. internal and external factors. The internal factors that influence the crop growth relate to the genetic capability of the seed or variety of crop being cultivated and the desirable characteristics for proper production are:
                    <p class="flow-text"></p>
                    • Excellent yielding ability<p class="flow-text"></p>
                    • Early maturity<p class="flow-text"></p>
                    • Resistance to diseases and lodging<p class="flow-text"></p>
                    • Tolerance to drought and pests<p class="flow-text"></p>
                    • Good chemical composition like oil and protein<p class="flow-text"></p>
                    • Tolerant to soil pH levels<p class="flow-text"></p>
                    • Good quality of produce or grains<p class="flow-text"></p>
                    • Good quality of straw<p class="flow-text"></p>
                    The external factors that influence the growth of the crop are mostly the environmental conditions of the region and could be subdivided into different factors like climatic, edaphic, biotic, physiographic and socio-economic.
                    <p class="flow-text"></p>
                    Climatic factors – precipitation, temperature, atmospheric humidity, <a target="https://www.agrifarming.in/solar-system-for-agricultural-water-pumps" href="https://www.agrifarming.in/solar-system-for-agricultural-water-pumps">solar</a> radiation, wind velocity and atmospheric gases.
                    <p class="flow-text"></p>
                    Edaphic factors – soil moisture, soil air, soil temperature, mineral matter, organic matter, organisms and reactions (pH).
                    <p class="flow-text"></p>
                    Biotic factors – plants and animals.
                    <p class="flow-text"></p>
                    Physiographic factors – topography, altitude, slope/steepness, exposure to wind and light.
                    <p class="flow-text"></p>
                    Socio-economic factors – choice of crops, availability of labour, discovery of new breeding varieties and economic condition of the farmer.</div>
              </li>
              <li>
                <div class="collapsible-header green-text text-darken-1">12. What are the methods of planting crops in the farm? </div>
                <div class="collapsible-body">There are two major ways in which planting could be done are direct seeding and transplanting. Direct seeding refers to planting the seeds in the fields for growing crops, whereas transplanting refers to planting saplings or seedlings that have been grown in nurseries from seeds.</div>
              </li>

              <li>
                <div class="collapsible-header green-text text-darken-1">13. What are broadcasting and transplanting?</div>
                <div class="collapsible-body">Scattering the seeds on the farm area either manually or mechanically is called broadcast seeding. This is exactly opposite or in contrast to precision seeding (planting seeds at the proper depth and place) and hydro seeding (spraying slurry of seeds, mulch and water in a uniform layer over prepared farm areas). The broadcasting method needs 10-20% extra seeds for sowing when compared to other sowing methods.

                  Transplanting is not associated with seeds rather it is planting of saplings or seedling onto the farm area. The seeds from which the saplings are obtained are generally sown in a nursery and grown under protected conditions.</div>
              </li>
              <li>
                  <div class="collapsible-header green-text text-darken-1">14. Why are grains dried before storage?</div>
                  <div class="collapsible-body">If grains that have been freshly harvested are stored without drying, then they may be spoiled due to micro-organism infestation. Drying the grains removes moisture from them so as to improve their shelf life and reduce the attack by insects, pests, bacteria and fungi. If the grains or seeds are to be used for sowing then moist grains lose the ability of germination very fast. So it is always advised to dry the seeds up to 12% before storing.</div>
              </li>

              <li>
                  <div class="collapsible-header green-text text-darken-1">15. Why are seeds treated before sowing?</div>
                  <div class="collapsible-body">Seeds are treated with chemicals prior to planting so as to protect them from microbial attacks and decay. The main aim of coating the seeds with insecticides and fungicides is to keep them safe from diseases. Chemicals are also coated over the seeds to increase the germinating ability of the seeds.</div>
              </li>

              <li>
                  <div class="collapsible-header green-text text-darken-1">16. Why do farmers use a combination of manures and fertilizers in their fields?</div>
                  <div class="collapsible-body">Manure is an organic matter which has many nutrients packed into a tremendously interconnected structure (like a web). Generally, if only a required nutrient is applied as fertilizer to the plants, then there is a possible risk of it being leached away or lost due to fixation. This results in insufficient supply of nutrient to the plant, but if fertilizer is applied long with manure then it remains in the soil structure (packed in the net like structure of manure) without getting leached and is made available for the plants.</div>
              </li>

              <li>
                  <div class="collapsible-header green-text text-darken-1">17. What is dibbling method?</div>
                  <div class="collapsible-body">Dibbling is a method of cultivation, which uses an instrument called dibbler to plant seeds into the soil. A dibbler is a pointed stick that creates hole in the ground and plants the seed or seedling into it. Dibbling is not just placing the seedlings into the hole; it is used in conjunction with another process called puddling. Puddling is planting a seedling into a small hole filled with water in the ground.</div>
              </li>


              <li>
                  <div class="collapsible-header green-text text-darken-1">18. What are the modern techniques of farming?</div>
                  <div class="collapsible-body">Modern farming techniques were deployed with two important goals in mind: one is to obtain higher yield and second to gain more economic profit. Six basic practices of agriculture are being developed through modern techniques, they are:
                    <p class="flow-text"></p>
                    • Intensive tillage<p class="flow-text"></p>
                    • Monoculture<p class="flow-text"></p>
                    • Use of synthetic fertilizer<p class="flow-text"></p>
                    • Improving irrigation technologies<p class="flow-text"></p>
                    • Chemical pest control<p class="flow-text"></p>
                    • Genetic manipulation</div>
              </li>
